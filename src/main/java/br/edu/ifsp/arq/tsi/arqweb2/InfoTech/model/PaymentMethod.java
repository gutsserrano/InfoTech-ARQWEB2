package br.edu.ifsp.arq.tsi.arqweb2.InfoTech.model;

public class PaymentMethod {

	private int id;
	private String name;
	
	public PaymentMethod() {
	
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
}
